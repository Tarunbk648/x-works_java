class Laptop {

    String brand;
    String model;
    String color;
    int price;
    int ram;
    int storage;
    String processor;
    double screenSize;
    String os;
    int battery;
    boolean fingerprint;
    int weight;

    Laptop(String brand,String model,String color,int price,int ram,int storage,String processor,double screenSize,String os,int battery,boolean fingerprint,int weight){

        this.brand=brand;
        this.model=model;
        this.color=color;
        this.price=price;
        this.ram=ram;
        this.storage=storage;
        this.processor=processor;
        this.screenSize=screenSize;
        this.os=os;
        this.battery=battery;
        this.fingerprint=fingerprint;
        this.weight=weight;
    }
}