class Shoe {

    String brand;
    String model;
    String color;
    int price;
    int size;
    String type;
    String material;
    double weight;
    boolean waterproof;
    String soleType;
    String laceType;
    String gender;

    Shoe(String brand,String model,String color,int price,int size,String type,
         String material,double weight,boolean waterproof,String soleType,
         String laceType,String gender){

        this.brand = brand;
        this.model = model;
        this.color = color;
        this.price = price;
        this.size = size;
        this.type = type;
        this.material = material;
        this.weight = weight;
        this.waterproof = waterproof;
        this.soleType = soleType;
        this.laceType = laceType;
        this.gender = gender;
    }
}