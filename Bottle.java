class Bottle {

    String brand;
    String color;
    int price;
    double capacity;
    String material;
    boolean insulated;
    double weight;
    String shape;
    boolean leakProof;
    String capType;
    double height;
    double diameter;

    Bottle(String brand,String color,int price,double capacity,String material,
           boolean insulated,double weight,String shape,boolean leakProof,
           String capType,double height,double diameter){

        this.brand = brand;
        this.color = color;
        this.price = price;
        this.capacity = capacity;
        this.material = material;
        this.insulated = insulated;
        this.weight = weight;
        this.shape = shape;
        this.leakProof = leakProof;
        this.capType = capType;
        this.height = height;
        this.diameter = diameter;
    }
}