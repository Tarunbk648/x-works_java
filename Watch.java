class Watch {

    String brand;
    String model;
    String color;
    int price;
    String type;
    boolean waterproof;
    String displayType;
    int batteryLife;
    String strapMaterial;
    String shape;
    boolean smartWatch;
    int weight;

    Watch(String brand,String model,String color,int price,String type,boolean waterproof,
          String displayType,int batteryLife,String strapMaterial,String shape,
          boolean smartWatch,int weight){

        this.brand = brand;
        this.model = model;
        this.color = color;
        this.price = price;
        this.type = type;
        this.waterproof = waterproof;
        this.displayType = displayType;
        this.batteryLife = batteryLife;
        this.strapMaterial = strapMaterial;
        this.shape = shape;
        this.smartWatch = smartWatch;
        this.weight = weight;
    }
}