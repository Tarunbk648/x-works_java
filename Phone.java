class Phone {

    String brand;
    String model;
    String color;
    int price;
    int storage;
    int battery;
    double screenSize;
    String processor;
    int ram;
    String os;
    int camera;
    boolean dualSim;

    Phone(String brand,String model,String color,int price,int storage,int battery,double screenSize,String processor,int ram,String os,int camera,boolean dualSim){
        this.brand=brand;
        this.model=model;
        this.color=color;
        this.price=price;
        this.storage=storage;
        this.battery=battery;
        this.screenSize=screenSize;
        this.processor=processor;
        this.ram=ram;
        this.os=os;
        this.camera=camera;
        this.dualSim=dualSim;
    }
}